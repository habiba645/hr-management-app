import 'package:flutter/material.dart';

Color clr(int colorNum) {
  switch (colorNum) {
    case 1:
      return Color(0xff232D5C);
    case 2:
      return Color(0xfffec81e);
    case 3:
      return Color(0xffb4b8c5);
    case 4:
      return Color(0xffe9ebf8);
    case 5:
      return Color(0xfffefffe);
  }
  return Colors.black;
}
